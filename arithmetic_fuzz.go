func FuzzBackendMultiplication(data []byte) int {
	if len(data) != 48*2 {
		return 0
	}
	a := new(fe).setBytes(data[:48])
	b := new(fe).setBytes(data[48:])
	for !a.isValid() {
		subAssign(a, &modulus)
	}
	for !b.isValid() {
		subAssign(b, &modulus)
	}
	c0, c1, c2 := new(fe), new(fe), new(fe)
	mulADX(c0, a, b)
	mulNoADX(c1, a, b)
	mulFallback(c2, a, b)
	if !c0.equal(c1) || !c0.equal(c2) {
		panic(fmt.Sprintf("a: %#x, b: %#x, c0: %#x, c1: %#x, c2: %#x", a.bytes(), b.bytes(), c0.bytes(), c1.bytes(), c1.bytes()))
	}
	return 1
}

